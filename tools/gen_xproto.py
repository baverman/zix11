#!/usr/bin/env python3

from __future__ import annotations

import xml.etree.ElementTree as ET
from dataclasses import dataclass
from pathlib import Path


XML_PATH = Path("/usr/share/xcb/xproto.xml")
OUT_PATH = Path("src/gen/xproto.zig")
TARGET_REQUESTS = ("InternAtom", "GetProperty", "GetInputFocus")

SCALAR_TYPES = {
    "BOOL": ("bool", 1),
    "BYTE": ("u8", 1),
    "CARD8": ("u8", 1),
    "CARD16": ("u16", 2),
    "CARD32": ("u32", 4),
    "char": ("u8", 1),
}


@dataclass(frozen=True)
class Field:
    kind: str
    name: str
    x_type: str
    zig_type: str
    size: int


@dataclass(frozen=True)
class Pad:
    bytes: int


@dataclass(frozen=True)
class ListField:
    name: str
    x_type: str
    zig_elem_type: str
    elem_size: int


@dataclass(frozen=True)
class ReplyField:
    name: str
    zig_type: str
    size: int
    offset: int


@dataclass(frozen=True)
class DerivedLengthField:
    name: str
    target_list: str
    size: int


def main() -> None:
    root = ET.parse(XML_PATH).getroot()
    requests = {child.attrib["name"]: child for child in root if child.tag == "request"}
    enums = {child.attrib["name"]: child for child in root if child.tag == "enum"}

    out = []
    emit(out, "// This file is generated by tools/gen_xproto.py.")
    emit(out, f"// Source: {XML_PATH}")
    emit(out, "")
    emit(out, 'const std = @import("std");')
    emit(out, 'const wire = @import("../wire.zig");')
    emit(out, "")
    emit(out, "pub const Atom = u32;")
    emit(out, "pub const Window = u32;")
    emit(out, "")

    emit_setup_types(out)
    emit(out, "")

    emit_enum(out, "GetPropertyType", enums["GetPropertyType"])
    emit(out, "")

    for request_name in TARGET_REQUESTS:
        emit_request_module(out, requests[request_name])
        emit(out, "")

    OUT_PATH.write_text("".join(out), encoding="utf-8")


def emit(buf: list[str], line: str) -> None:
    buf.append(line + "\n")


def emit_enum(buf: list[str], name: str, node: ET.Element) -> None:
    value = 0
    emit(buf, f"pub const {name} = enum(u32) {{")
    for item in node.findall("item"):
        item_name = item.attrib["name"]
        value_node = item.find("value")
        if value_node is not None and value_node.text is not None:
            value = int(value_node.text.strip(), 0)
        emit(buf, f"    {zig_name(item_name)} = {value},")
        value += 1
    emit(buf, "    _,")
    emit(buf, "};")


def emit_setup_types(buf: list[str]) -> None:
    emit(buf, "pub const SetupRequest = struct {")
    emit(buf, "    byteOrder: u8,")
    emit(buf, "    protocolMajorVersion: u16,")
    emit(buf, "    protocolMinorVersion: u16,")
    emit(buf, "    authorizationProtocolName: []const u8,")
    emit(buf, "    authorizationProtocolData: []const u8,")
    emit(buf, "")
    emit(buf, "    pub fn encode(self: @This(), writer: *std.Io.Writer) wire.Error!void {")
    emit(buf, "        try writer.writeByte(self.byteOrder);")
    emit(buf, "        try writer.writeByte(0);")
    emit(buf, "        try writer.writeInt(u16, self.protocolMajorVersion, .little);")
    emit(buf, "        try writer.writeInt(u16, self.protocolMinorVersion, .little);")
    emit(buf, "        try writer.writeInt(u16, @intCast(self.authorizationProtocolName.len), .little);")
    emit(buf, "        try writer.writeInt(u16, @intCast(self.authorizationProtocolData.len), .little);")
    emit(buf, "        try writer.writeInt(u16, 0, .little);")
    emit(buf, "        try writer.writeAll(self.authorizationProtocolName);")
    emit(buf, "        try writer.splatByteAll(0, wire.pad4(self.authorizationProtocolName.len));")
    emit(buf, "        try writer.writeAll(self.authorizationProtocolData);")
    emit(buf, "        try writer.splatByteAll(0, wire.pad4(self.authorizationProtocolData.len));")
    emit(buf, "    }")
    emit(buf, "};")
    emit(buf, "")

    emit(buf, "pub const Setup = struct {")
    emit(buf, "    status: u8,")
    emit(buf, "    protocolMajorVersion: u16,")
    emit(buf, "    protocolMinorVersion: u16,")
    emit(buf, "    releaseNumber: u32,")
    emit(buf, "    resourceIdBase: u32,")
    emit(buf, "    resourceIdMask: u32,")
    emit(buf, "    motionBufferSize: u32,")
    emit(buf, "    maximumRequestLength: u16,")
    emit(buf, "    rootsLen: u8,")
    emit(buf, "    pixmapFormatsLen: u8,")
    emit(buf, "    imageByteOrder: u8,")
    emit(buf, "    bitmapFormatBitOrder: u8,")
    emit(buf, "    bitmapFormatScanlineUnit: u8,")
    emit(buf, "    bitmapFormatScanlinePad: u8,")
    emit(buf, "    minKeycode: u8,")
    emit(buf, "    maxKeycode: u8,")
    emit(buf, "    root: Window,")
    emit(buf, "")
    emit(buf, "    pub fn decode(reader: *std.Io.Reader) wire.Error!@This() {")
    emit(buf, "        const status = try reader.takeByte();")
    emit(buf, "        if (status != 1) return error.UnexpectedReplyType;")
    emit(buf, "        _ = try reader.takeByte();")
    emit(buf, "        const protocol_major_version = try reader.takeInt(u16, .little);")
    emit(buf, "        const protocol_minor_version = try reader.takeInt(u16, .little);")
    emit(buf, "        const length_words = try reader.takeInt(u16, .little);")
    emit(buf, "        const extra_len = @as(usize, length_words) * 4;")
    emit(buf, "        const release_number = try reader.takeInt(u32, .little);")
    emit(buf, "        const resource_id_base = try reader.takeInt(u32, .little);")
    emit(buf, "        const resource_id_mask = try reader.takeInt(u32, .little);")
    emit(buf, "        const motion_buffer_size = try reader.takeInt(u32, .little);")
    emit(buf, "        const vendor_len = try reader.takeInt(u16, .little);")
    emit(buf, "        const maximum_request_length = try reader.takeInt(u16, .little);")
    emit(buf, "        const roots_len = try reader.takeByte();")
    emit(buf, "        const pixmap_formats_len = try reader.takeByte();")
    emit(buf, "        const image_byte_order = try reader.takeByte();")
