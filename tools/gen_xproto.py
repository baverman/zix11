def emit_request_module(buf: list[str], request: ET.Element) -> None:
    name = request.attrib["name"]
    opcode = int(request.attrib["opcode"])
    body_nodes = [node for node in request if node.tag in {"field", "pad", "list"}]
    reply = request.find("reply")
    request_model = parse_request_body(body_nodes)

    emit(buf, f"pub const {name}Request = struct {{")
    emit(buf, f"    pub const opcode: u8 = {opcode};")
    emit(buf, "")
    for line in request_field_lines(request_model):
        emit(buf, line)
    if request_field_lines(request_model):
        emit(buf, "")
    for line in request_encode_lines(request_model):
        emit(buf, line)
    emit(buf, "};")

    if reply is not None:
        emit(buf, "")
        for line in reply_lines(
            name,
            parse_reply_body([node for node in reply if node.tag in {"field", "pad", "list"}]),
        ):
            emit(buf, line)


def request_field_lines(model: dict) -> list[str]:
    lines: list[str] = []
    if model["header_field"] is not None:
        field: Field = model["header_field"]
        lines.append(f"    {zig_name(field.name)}: {field.zig_type},")
    for item in model["body"]:
        if isinstance(item, Field):
            lines.append(f"    {zig_name(item.name)}: {item.zig_type},")
        elif isinstance(item, DerivedLengthField):
            continue
        elif isinstance(item, ListField):
            lines.append(f"    {zig_name(item.name)}: []const {item.zig_elem_type},")
    return lines


def request_encode_lines(model: dict) -> list[str]:
    body_bytes_expr: list[str] = []
    byte_len_uses_self = False
    for item in model["body"]:
        if isinstance(item, Field):
            body_bytes_expr.append(str(item.size))
        elif isinstance(item, DerivedLengthField):
            body_bytes_expr.append(str(item.size))
        elif isinstance(item, Pad):
            body_bytes_expr.append(str(item.bytes))
        elif isinstance(item, ListField):
            name = zig_name(item.name)
            body_bytes_expr.append(f"self.{name}.len")
            body_bytes_expr.append(f"wire.pad4(self.{name}.len)")
            byte_len_uses_self = True

    lines = [
        "    pub fn byteLen(self: @This()) usize {",
    ]
    if not byte_len_uses_self:
        lines.append("        _ = self;")
    lines.extend([
        f"        return 4 + {' + '.join(body_bytes_expr) if body_bytes_expr else '0'};",
        "    }",
        "",
        "    pub fn encode(self: @This(), writer: *std.Io.Writer) wire.Error!void {",
        "        try writer.writeByte(opcode);",
    ])
    header_field: Field | None = model["header_field"]
    if header_field is None:
        lines.append("        try writer.writeByte(0);")
    elif header_field.zig_type == "bool":
        lines.append(f"        try writer.writeByte(@intFromBool(self.{zig_name(header_field.name)}));")
    else:
        lines.append(f"        try writer.writeByte(self.{zig_name(header_field.name)});")
    lines.append("        try writer.writeInt(u16, @intCast(self.byteLen() / 4), .little);")
    if not model["body"]:
        lines.append("        return;")
        lines.append("    }")
        return lines
    for item in model["body"]:
        if isinstance(item, Field):
            name = zig_name(item.name)
            if item.zig_type == "bool":
                lines.append(f"        try writer.writeByte(@intFromBool(self.{name}));")
            elif item.size == 1:
                lines.append(f"        try writer.writeByte(self.{name});")
            elif item.size == 2:
                lines.append(f"        try writer.writeInt(u16, self.{name}, .little);")
            elif item.size == 4:
                lines.append(f"        try writer.writeInt(u32, self.{name}, .little);")
        elif isinstance(item, DerivedLengthField):
            list_name = zig_name(item.target_list)
            if item.size == 2:
                lines.append(f"        try writer.writeInt(u16, @intCast(self.{list_name}.len), .little);")
            elif item.size == 4:
                lines.append(f"        try writer.writeInt(u32, @intCast(self.{list_name}.len), .little);")
            else:
                raise AssertionError(item)
        elif isinstance(item, Pad):
            lines.append(f"        try writer.splatByteAll(0, {item.bytes});")
        elif isinstance(item, ListField):
            list_name = zig_name(item.name)
            lines.append(f"        try writer.writeAll(self.{list_name});")
            lines.append(f"        try writer.splatByteAll(0, wire.pad4(self.{list_name}.len));")
    lines.append("    }")
    return lines


def reply_lines(name: str, model: dict) -> list[str]:
    lines: list[str] = [f"pub const {name}Reply = struct {{"]
    for field in model["fields"]:
        lines.append(f"    {zig_name(field.name)}: {field.zig_type},")
    if model["list"] is not None:
        list_field: ListField = model["list"]
        lines.append(f"    {zig_name(list_field.name)}: []const {list_field.zig_elem_type},")
    lines.append("")
    if model["list"] is None:
        lines.extend(reply_decode_fixed_lines(model))
    else:
        lines.extend(reply_decode_variable_lines(model))
    lines.append("};")
    return lines


def reply_decode_fixed_lines(model: dict) -> list[str]:
    lines = [
        "    pub fn decode(reader: *std.Io.Reader) wire.Error!@This() {",
        "        if (try reader.takeByte() != 1) return error.UnexpectedReplyType;",
        "        _ = try reader.takeByte();",
        "        _ = try reader.takeInt(u16, .little);",
        "        _ = try reader.takeInt(u32, .little);",
        "        const result: @This() = .{",
    ]
    for field in model["fields"]:
        accessor = reply_field_accessor(field)
        lines.append(f"            .{zig_name(field.name)} = {accessor},")
    fixed_field_bytes = sum(field.size for field in model["fields"])
    trailing = 24 - fixed_field_bytes
    lines.append("        };")
    if trailing > 0:
        lines.append(f"        _ = try reader.take({trailing});")
    lines.append("        return result;")
    lines.append("    }")
    return lines


def reply_decode_variable_lines(model: dict) -> list[str]:
    list_field: ListField = model["list"]
    lines = [
        "    pub fn decode(reader: *std.Io.Reader, scratch: []u8) wire.Error!@This() {",
        "        if (try reader.takeByte() != 1) return error.UnexpectedReplyType;",
        "        const format = try reader.takeByte();",
        "        _ = try reader.takeInt(u16, .little);",
        "        const reply_len = @as(usize, try reader.takeInt(u32, .little)) * 4;",
        "        const type_atom = try reader.takeInt(Atom, .little);",
        "        const bytes_after = try reader.takeInt(u32, .little);",
        "        const value_len = try reader.takeInt(u32, .little);",
        "        _ = try reader.take(12);",
        "        const value_bytes = try wire.valueByteLen(format, value_len);",
        "        if (reply_len < value_bytes) return error.MalformedPacket;",
        "        if (scratch.len < value_bytes) return error.BufferTooSmall;",
        "        @memcpy(scratch[0..value_bytes], try reader.take(value_bytes));",
        "        _ = try reader.take(reply_len - value_bytes);",
        "        return .{",
        "            .format = format,",
        "            .type_atom = type_atom,",
        "            .bytesAfter = bytes_after,",
        "            .valueLen = value_len,",
        f"            .{zig_name(list_field.name)} = scratch[0..value_bytes],",
        "        };",
        "    }",
    ]
    return lines


def reply_field_accessor(field: ReplyField) -> str:
    if field.name == "format":
        return "try reader.takeByte()"
    if field.name == "revert_to":
        return "try reader.takeByte()"
    if field.size == 1:
        return "try reader.takeByte()"
    if field.size == 2:
        return "try reader.takeInt(u16, .little)"
    if field.size == 4:
        return f"try reader.takeInt({field.zig_type}, .little)"
    raise AssertionError(field)


def parse_request_body(nodes: list[ET.Element]) -> dict:
    header_field: Field | None = None
    body: list[Field | Pad | ListField | DerivedLengthField] = []
    index = 0
    if nodes and nodes[0].tag == "field":
        field = parse_field(nodes[0])
        if field.size == 1:
            header_field = field
            index = 1
    while index < len(nodes):
        node = nodes[index]
        if node.tag == "field":
            field = parse_field(node)
            target_list = inferred_list_name(field.name, nodes[index + 1 :])
            if target_list is not None:
                body.append(DerivedLengthField(name=field.name, target_list=target_list, size=field.size))
            else:
                body.append(field)
        elif node.tag == "pad":
            body.append(Pad(bytes=pad_bytes(node)))
        elif node.tag == "list":
            body.append(parse_list(node))
        index += 1
    return {"header_field": header_field, "body": body}


def parse_reply_body(nodes: list[ET.Element]) -> dict:
    fields: list[ReplyField] = []
    list_field: ListField | None = None
    offset = 8
    if nodes:
        first = nodes[0]
        if first.tag == "field" and scalar_size(first.attrib["type"]) == 1:
            field = parse_field(first)
            fields.append(ReplyField(name=field.name, zig_type=field.zig_type, size=field.size, offset=1))
            nodes = nodes[1:]
        elif first.tag == "pad" and pad_bytes(first) == 1:
            nodes = nodes[1:]
    for node in nodes:
        if node.tag == "field":
            field = parse_field(node)
            fields.append(ReplyField(name=field.name, zig_type=field.zig_type, size=field.size, offset=offset))
            offset += field.size
        elif node.tag == "pad":
            offset += pad_bytes(node)
        elif node.tag == "list":
            list_field = parse_list(node)
    return {"fields": fields, "list": list_field}


def parse_field(node: ET.Element) -> Field:
    x_type = node.attrib["type"]
    zig_type, size = zig_scalar_type(x_type)
    return Field(kind="field", name=node.attrib["name"], x_type=x_type, zig_type=zig_type, size=size)


def parse_list(node: ET.Element) -> ListField:
    x_type = node.attrib["type"]
    zig_type, size = zig_scalar_type(x_type)
    return ListField(name=node.attrib["name"], x_type=x_type, zig_elem_type=zig_type, elem_size=size)


def zig_scalar_type(x_type: str) -> tuple[str, int]:
    if x_type in {"ATOM", "WINDOW"}:
        return x_type.title().replace("_", ""), 4
    if x_type == "void":
        return "u8", 1
    if x_type in SCALAR_TYPES:
        return SCALAR_TYPES[x_type]
    raise ValueError(f"unsupported scalar type: {x_type}")


def scalar_size(x_type: str) -> int:
    return zig_scalar_type(x_type)[1]


def pad_bytes(node: ET.Element) -> int:
    if "bytes" in node.attrib:
        return int(node.attrib["bytes"], 0)
    raise ValueError(f"unsupported pad node: {ET.tostring(node, encoding='unicode')}")


def inferred_list_name(field_name: str, later_nodes: list[ET.Element]) -> str | None:
    if not field_name.endswith("_len"):
        return None
    candidate = field_name[: -len("_len")]
    for node in later_nodes:
        if node.tag == "list" and node.attrib["name"] == candidate:
            return candidate
    return None


def zig_name(name: str) -> str:
    if name == "type":
        return "type_atom"
    if name == "delete":
        return "delete_value"
    parts = name.split("_")
    return parts[0] + "".join(part.capitalize() for part in parts[1:])


